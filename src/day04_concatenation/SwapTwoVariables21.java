package day04_concatenation;

public class SwapTwoVariables21 {
    public static void main(String[] args) {
        int x = 10;
        int y = 15;

        System.out.println("x = " + x);
        System.out.println("y = " + y);

        System.out.println("**************");

        System.out.println("x = " + y);
        System.out.println("y = " + x);

    }
}
